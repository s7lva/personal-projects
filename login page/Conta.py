class Conta:
    def __init__(self, nome , email , pin, numerotelemovel,):
        self.nome = nome
        self.email = email
        self.pin = pin
        self.numerotelemovel = numerotelemovel

    def Nome(self):
        return self.nome
    
    def Email(self):
        return self.email
    
    def Pin(self):
        return self.pin
    
    def Numerotelemovel(self):
        return self.numerotelemovel
    
    

    